// faça um programa que leia um numero, incremente 1 a esse numero e o imprima
#include <iostream>
using namespace std; //delicia

short num; // define a ariavel como short,

int main()
{
    cout << "Digite um numero!\n"; // printa uma mensagem
    cin >> num; // coleta um valor digitado e grava na variavel
    num++; // acresenta 1 ao numero
    cout << num; // printa o valor armazenado na variavel
}
